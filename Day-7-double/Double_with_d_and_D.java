class Double_with_d_and_D
{
	public static void main(String args[])
	{
		/*we can use 'D' or 'd' as a prefix while declaring an double data type in java programming*/
		double x = 12.34D;
		System.out.println(x); // it will display the value of x
		
		double y = 1234.987d;
		System.out.println(y); // it will display the value of y
	}
}